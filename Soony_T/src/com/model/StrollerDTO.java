package com.model;

public class StrollerDTO {
	private String str_num;
	private String id;
	
	public StrollerDTO(String str_num, String id) {
		super();
		this.str_num = str_num;
		this.id = id;
	}

	public String getStr_num() {
		return str_num;
	}

	public void setStr_num(String str_num) {
		this.str_num = str_num;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}
	
	
 
	
}
